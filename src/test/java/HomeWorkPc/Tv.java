package HomeWorkPc;
public class Tv {
    private String model;
    private String displayTchnology;
    private String resolution;
    private String color;
    private String displayDiagonal;

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getDisplayTchnology() {
        return displayTchnology;
    }

    public void setDisplayTchnology(String displayTchnology) {
        this.displayTchnology = displayTchnology;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getDisplayDiagonal() {
        return displayDiagonal;
    }

    public void setDisplayDiagonal(String displayDiagonal) {
        this.displayDiagonal = displayDiagonal;
    }
}
