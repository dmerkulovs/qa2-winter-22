package HomeWorkPc;
public class Headphones {
    private String model;
    private String earCoupling;
    private String resolution;
    private String color;
    private String wearingStyle;
    private int wight;

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getEarCoupling() {
        return earCoupling;
    }

    public void setEarCoupling(String earCoupling) {
        this.earCoupling = earCoupling;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getWearingStyle() {
        return wearingStyle;
    }

    public void setWearingStyle(String wearingStyle) {
        this.wearingStyle = wearingStyle;
    }

    public int getWight() {
        return wight;
    }

    public void setWight(int wight) {
        this.wight = wight;
    }
}
